function sortBy() {
  var selected = document.getElementById("selectRelevance").value;
  console.log("hi");
  if (selected == "low") {
    products = products.sort(function (a, b) {
      return a.MRP - b.MRP;
    });
  }
  if (selected == "high") {
    products = products.sort(function (a, b) {
      return b.MRP - a.MRP;
    });
  }
  if (selected == "alphabetical") {
    console.log(selected);
    products = products.sort(function (a, b) {
      return a.name > b.name ? 1 : b.name > a.name ? -1 : 0;
    });
  }
  show(products);
}

